package programming5;

public class Employee {
	private String name, phone;
	private int salary;
	
	public void setname(String input_name) {
		name = input_name;
	}

	public void setphone(String input_phone) {
		phone = input_phone;
	}

	public void setsalary(int input_salary) {
		salary = input_salary;
	}

	public String getname() {
		return name;
	}

	public String getphone() {
		return phone;
	}

	public int getsalary() {
		return salary;
	}
	


}
