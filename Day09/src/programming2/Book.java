package programming2;

public class Book {
	private String title, authur;
	
	
	public void setTitle(String input_title) {
		title = input_title;
	}

	public void setAuthur(String input_authur) {
		authur = input_authur;
	}


	public String getTitle(){
		return title;
	}
	
	public String getAuthur(){
		return authur;
	}
	


}
